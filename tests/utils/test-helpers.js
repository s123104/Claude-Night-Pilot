/**
 * Á≠âÂæÖ‰∏¶Êì∑ÂèñÈåØË™§Êó•Ë™å
 * @param {Page} page - Playwright Page Áâ©‰ª∂
 * @returns {Array} ÈåØË™§Êó•Ë™åÈô£Âàó
 */
export async function captureErrorLogs(page) {
  const errors = [];
  
  page.on('console', (msg) => {
    if (msg.type() === 'error') {
      errors.push(msg.text());
    }
  });
  
  page.on('pageerror', (error) => {
    errors.push(error.message);
  });
  
  return errors;
}

